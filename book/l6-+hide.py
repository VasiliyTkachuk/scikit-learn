import numpy as np

# ---------------------------------------------------------------------------
# Двухслойная сеть (со «скрытым» слоем) вместо одной матрицы весов, как в l5-few.py.
#
# l5:  input(3) --[одна матрица weights 3×3]--> pred(3)
# l6:  input(3) --[ih_wgt 3×3]--> hid(3) --[hp_wgt 3×3]--> pred(3)
#
# Промежуточный вектор hid не виден снаружи — его считает сама сеть.
# Название урока «+hide» — добавили скрытый шаг между входом и выходом.
# ---------------------------------------------------------------------------

# ih_wgt (input → hidden): веса от трёх входов к трём скрытым нейронам.
# Форма (3, 3): строка i — вклад i-го входа во все скрытые; столбец j — веса
# всех входов в j-й скрытый нейрон.
ih_wgt = np.array([
    [0.1, 0.2, -0.1],   # от 1-го входа (toes)  к трём скрытым
    [-0.1, 0.1, 0.9],   # от 2-го входа (wlrec)
    [0.1, 0.4, 0.1]     # от 3-го входа (hfans)
])

# hp_wgt (hidden → prediction): веса от скрытого слоя к выходу.
# Тоже (3, 3): три скрытых нейрона порождают три выходных значения pred.
hp_wgt = np.array([
    [0.3, 1.1, -0.3],   # от 1-го скрытого к трём выходам
    [0.1, 0.2, 0.0],    # от 2-го скрытого
    [0.0, 1.3, 0.1]     # от 3-го скрытого
])

# weights[0] — первый слой, weights[1] — второй; порядок = порядок прохода сигнала.
weights = [ih_wgt, hp_wgt]

def neural_network(input, weights):
    # Шаг 1 — скрытый слой.
    # input: (3,), weights[0]: (3, 3) → hid: (3,)
    # hid[j] = input[0]*w[0,j] + input[1]*w[1,j] + input[2]*w[2,j]
    # Каждый скрытый нейрон — своя линейная комбинация тех же трёх входов, но с другими весами.
    hid = input.dot(weights[0])

    # Шаг 2 — выход.
    # hid: (3,), weights[1]: (3, 3) → pred: (3,)
    # Те же три числа снова смешиваются, уже через вторую матрицу.
    pred = hid.dot(weights[1])
    return pred

# ---------------------------------------------------------------------------
# Признаки по четырём матчам; для примера берём только первый матч (индекс 0).
# ---------------------------------------------------------------------------

toes = np.array([   # доля игроков без травм (в книге — toes)
    8.5, 9.5, 9.9, 9.0
])

wlrec = np.array([  # win/loss record — показатель побед
    0.65, 0.8, 0.8, 0.9
])

hfans = np.array([  # «счастье» болельщиков (суррогатный признак)
    1.2, 1.3, 0.5, 1.0
])

# Один пример: три числа в один вектор (строка матрицы признаков).
input = np.array([toes[0], wlrec[0], hfans[0]])

pred = neural_network(input, weights)

# pred — вектор из трёх чисел (не одно скалярное предсказание).
# Пока нет sigmoid/ReLU между слоями — цепочка всё ещё одна большая линейная
# операция, но разбитая на два шага: сначала «перекодировать» вход в hid,
# потом hid в pred. С нелинейностью между слоями сеть сможет учить более сложные закономерности.
print(pred)
